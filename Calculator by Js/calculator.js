
// -----------------------Addition---------------------
function add(){
    let a = parseInt(prompt(`Enter First Number `))
    let b = parseInt(prompt(`Enter Second Number `))
    let c = a + b;
    alert(`The Addition of ${a} and ${b} is: ${c}`)
}


// ------------------------Subtraction---------------------
function sub(){
    let d = parseInt(prompt(`Enter First Number `))
    let e = parseInt(prompt(`Enter Second Number `))
    let f = d - e;
    alert(`The Subtraction of ${d} and ${e} is: ${f}`)
}


// --------------------------Multiplication------------------
function multi(){
    let g = parseInt(prompt(`Enter First Number `))
    let h = parseInt(prompt(`Enter Second Number `))
    let j = g * h;
    alert(`The Multiplication of ${g} and ${h} is: ${j}`)
}


